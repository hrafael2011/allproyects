

def caculadora(valor1, valor2):

    suma = valor1+valor2
    resta = valor1-valor2
    multi = valor1*valor2
    divi = valor1/valor2

    print(f'El resultado de la suma:{suma} , resta {resta},  multiplicacion {multi}, division: {divi}')

val1 = int(input('Inserte un el valor 1'))
